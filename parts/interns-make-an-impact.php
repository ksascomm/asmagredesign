			<p><span class="dropcap">W</span>ith its pastel-striped paint job and dusty vinyl seats, the Baltimore City Public Health Department‚Äôs Needle Exchange van feels more like a camper you‚Äôd take to a state park than a pioneering site for HIV research and prevention.</p>
			
			<p>Inside, the air conditioning runs full blast, the white noise of the fan all but obscuring the retro rhythm and blues songs on the radio. Outside, several men and women carrying plastic bags linger near the unmarked van, parked as it is every Monday morning next to the Monroe Street United Methodist Church. Across the street sits a string of rowhouses whose front sidewalks are littered with weeds, a pile of old tires, a baby carriage‚Äîthe everyday urban detritus that makes this patch of Southwest Baltimore look no different than any of the city‚Äôs other underserved neighborhoods.</p>
			
			<p>Three days a week, the staff of the Health Department‚Äôs Community Risk Reduction Services Program visits this corner at the intersection of Monroe and Ramsay streets. Inside the program‚Äôs van, they meet with people addicted to drugs injected intravenously to offer free HIV testing, distribute Harm Reduction Kits, and exchange clean needles for used ones. On Mondays, Johns Hopkins senior Anita Ram is on board.</p>
			
			<p>Dressed casually but neatly, in jeans and a light blue T-shirt celebrating ‚ÄúJHU President‚Äôs Day of Service, 10.02.10,‚Äù Ram looks every inch a 21-year-old, yet her maturity sets her apart. With an easy smile, she greets clients who enter the van one at a time and records their personal information, before asking how many needles they have to exchange and whether they want long or short syringes.</p>
				
				<div class="sidequote">
					<p>As an intern with the Health Department‚Äôs Needle Exchange Program, Anita Ram worked hard to gain the trust of Baltimore‚Äôs intravenous drug users. </p>
				</div>
			
			<p>When a man with pale blue eyes and a navy bandanna reports police seizing his clean needles and ID card, she asks if he would like to participate in a Health Department survey designed to track clients&#39; interactions with law enforcement. When another young man, obviously high, agitated, and with an open wound on his shin, insists that he&#39;s found information on the Internet that says distributing clean needles is illegal, Ram listens closely and gently corrects him before exchanging the 100 needles he&#39;s brought in.</p>

			<p>&#8220;Thanks. See you Wednesday,&#8221; he says, as he leaves.</p>
			
			<p>In operation since 1994, the Needle Exchange Program has been instrumental in lowering the number of new HIV infections related to intravenous drug use from 650 reported cases in Baltimore in 1994 to 177 new cases in 2010. The program, the only confidential syringe exchange program in the nation, has also distributed more than 9 million clean needles and collected more than 10 million contaminated needles to be safely destroyed. Ram is clearly proud of the program&#39;s success, and during lulls between clients, she talks animatedly about a needle exchange program in Washington, D.C., and another she read about in a New York Times article.</p>
			
			<p>Any way you look at it, Ram is an accomplished student. A public health major from Columbia, Maryland, she is one of the co-presidents of Johns Hopkins&#39; Women&#39;s Pre-health Leadership Society, has volunteered at the Baltimore Rescue Mission clinic for homeless men, and spent last summer in Chennai, India, the city where her parents grew up, helping to organize an outreach camp and educate parents about child nutrition. (After the conclusion of her internship, Ram returned to India through a Provost&#39;s Undergraduate Research Award (PURA) grant to study the increasing prevalence of obesity in Chennai.) She plans to attend medical school after taking a year off.</p>
			
			<p>Ram says she was motivated to apply for her Community Impact Internship because she felt that living on campus was a bit like living in a bubble. She wanted to learn more about the city, she says, and the position with Community Risk Reduction Services felt like a natural choice because of its focus on epidemiology and community health, two areas in which Ram has a keen interest.</p>
			
			<p>&#8220;I probably learned more [about Baltimore] in one and a half weeks than in three years in living here &hellip; [there are so many] social issues I had a blind eye toward,&#8221; she says.</p>
			
			<p>Along with spending time at Monroe and Ramsay streets, Ram travels once a week on the van that visits the Block, the Red Light District on Baltimore Street where prostitution and strip clubs prevail. She&#39;s found that clients here are eager to take advantage of the many services&#8212;including distribution of birth control and other reproductive health services&#8212;that the mobile unit has to offer.</p>
			
			<p>Ram is also actively involved in the &#8220;Aligning Law Enforcement and HIV Prevention in Baltimore City&#8221; study sponsored by the Bloomberg School of Public Health, which focuses on police attitudes and beliefs about needle exchange via a two-pronged survey of Needle Exchange clients and police officers who receive harm reduction training from BCHD staff. Ram, who conducts client surveys in the van, is quick to point out the need for abundant listening and patience. &#8220;You can&#39;t just go out in the community and say, ‚ÄòHey tell me about your issues,&#39;&#8221; she says. &#8220;You have to gain people&#39;s trust.&#8221;</p>
			
			<p>Chris Serio-Chapman, director of Community Risk Reduction Services, describes Ram as having &#8220;an eagerness and a desire to change the world.&#8221;</p>
			
			<p>&#8220;It&#39;s specific to Anita,&#8221; she says. &#8220;She is not numb. She is like ‚ÄòGood gosh, we have to do something about that!&#39;</p>
			
			<p>&#8220;She reminds us about what is important,&#8221; says Serio-Chapman. &#8220;You forget after 17 years of doing this.&#8221;</p>
