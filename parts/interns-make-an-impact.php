			<p>Founded in 2000, Martha&#39;s Place offers six&#8209;month transitional housing and long&#8209;term single room occupancy (SRO) housing in two snug West Baltimore rowhomes to women beset by drug addiction and homelessness. Recovery rates here are above the national average of 30 percent, with a 50 percent recovery success attributed to the six&#8209;month program, and 75 percent recovery attributed to the SRO. Tonight, seven women&#8212;five residents of the six&#8209; month program and two from the SRO house&#8212;share the evening meal.</p>
			
			<p>This kind of intimacy can be scary. Taken on that level, Duval&#39;s hesitation was understandable. It wasn&#39;t that she was disinterested in public service. Duval, a Baltimore Scholar from the city&#39;s Hamilton neighborhood, had been a volunteer with Youth Opportunity Baltimore during her junior year, and had performed service projects with Hopkins Christian Fellowship. But she hadn&#39;t engaged in any work she could characterize as &#8220;long term and serious.&#8221;</p>
			
