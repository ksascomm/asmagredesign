				<div class="sidequote">
					<p>Dominique Duval says her work with women at Martha&#39;s Place, in West Baltimore, challenged her perceptions about drug addiction.</p>
				</div>
			<p>Still, Duval faced challenges, some geographic, some self&#8209;imposed. Having lived mostly on Baltimore&#39;s east side, the west side was unfamiliar. Duval also saw her background as much more privileged than that of the residents and worried whether she could connect with them. And then there was the challenge of working with women battling addiction.</p>
			
			<p>Even her supervisors harbored some doubts about Duval&#39;s placement. Martha&#39;s Place executive director Angela Long recalls Duval&#39;s initial hesitation in interacting with the residents. But during the internship&#39;s first week, Martha&#39;s Place celebrated three residents&#39; graduations from the six&#8209;month program, and Duval found herself moved by the women&#39;s testimonies and transformations.</p>
			
			<p>Since then, she has embraced the mission of Martha&#39;s Place and is involved in all aspects of administrative work, from taking phone calls to keeping documents up&#8209;to&#8209;date. She also assists in house management, and this Wednesday evening finds her sitting down to dinner with residents, looking at family photos, and joining in the wave of laughter that sweeps through the table when one of the women pretends to break a tooth on a slice of burned garlic toast.</p>
			
