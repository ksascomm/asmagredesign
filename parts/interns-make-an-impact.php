<p><span class="dropcap">&#8220;Y</span>ou probably think you know how to skip,&#8221; says Joanna Craig to the 12 little girls in multicolored leotards and ballet slippers circled around her, &#8220;but you&#39;ve never done a ballet skip.&#8221;</p>

<p>Craig gracefully demonstrates the step before sending the girls skipping, arms extended, across the blonde wood studio floor of the Harris&#8209;Marcus Center on Pennsylvania Avenue in West Baltimore. The center is home to Jubilee Arts, a community organization that provides arts classes at little or no cost to low&#8209;income residents of the Sandtown&#8209;Winchester and Upton communities. Classes are available for both children and adults, but it&#39;s hard to imagine the adults embracing the music and the movement with as much freedom and joy as these young girls do. Even the formal rules of ballet have a liberating effect that television or computers can&#39;t begin to match.</p>

<p>&#8220;Step, hop,&#8221; Craig reminds the girls quietly as they dance, some concentrating on their feet to get the right steps, other leaping like tiny frogs.</p>

<p>Skipping, Craig explains later, is the girls&#39; favorite activity.</p>

<p>This ballet class is different from the ones Craig, 21, has taught at other places, where classes were held in a gym and became some&#8209; thing to occupy students after school. Here, says Craig, students have signed up for the class because they have high aspirations as dancers. They&#39;ve begun to recognize steps after they&#39;ve performed them each week. &#8220;They all really want to be ballerinas,&#8221; she says.</p>
				
				<div class="sidequote">
					<p>Joanna Craig bonded quickly with the budding young dancers she taught through the Jubilee Arts program. &#8220;They all really want to be ballerinas,&#8221; she says.</p>
				</div>
<p>Fair&#8209;haired and dressed in black footless tights and a pink top, Craig, a senior from New York City majoring in behavioral biology, looks every bit the dance instructor. A member of Johns Hopkins&#39; Modern Dance Company, she is a bit reserved, and she considers questions carefully before answering. But she blossoms when she interacts with the students&#8212;reaching out to hug a girl who throws her arms around her waist, brightly asking another child whether she&#39;d like a blue or red leotard from the bag of donated dance clothes.</p>

<p>Mariska Jordan, Jubilee Arts&#39; director, recalls an afternoon when a scheduled hip&#8209;hop class had to be canceled and one child lingered in the studio, upset that she wouldn&#39;t be dancing.</p> 

<p>Before you knew it, Jordan says, Craig and the girl were exchanging dance moves, a ballet step for a hip&#8209;hop step. &#8220;Even though that wasn&#39;t Joanna&#39;s class, she stuck around and became engaged,&#8221; says Jordan.</p>

<p>Teaching dance is only part of Craig&#39;s assignments at Jubilee Arts. Since her internship began, she&#39;s also been involved in organizing rosters and mailing lists, making sponsorship calls, and leading the summer food program that provides free lunch between noon and 2 p.m. to neighborhood children.</p>

<p>Although some aspects of the work have been frustrating&#8212;trying to get possible sponsors to return phone calls and emails, convincing the community to take advantage of the services offered&#8212;Craig has gotten what she wanted and more out of this internship. She&#39;s seen the ins and outs of a non&#8209; pro‚ê£t, and how hard the staff must work.</p>

<p>Craig has also come to value the physical place of nonprofits in the community and says she&#39;s learned that &#8220;the best way to get to know people isn&#39;t necessarily teaching in a class or in a formal setting.&#8221;</p>

<p>&#8220;It&#39;s sitting down at a table and chair for a little bit,&#8221; she explains. &#8220;Talking to someone, you can learn so much about them.&#8221;</p>

<p>When Craig began her internship, she observed other interns who had been there for close to a year and saw them interacting with clients by name and asking how their children were. &#8220;I want[ed] to get a little bit of that,&#8221; she recalls.</p>

<p>In a very short time, she has gotten her wish.</p>
